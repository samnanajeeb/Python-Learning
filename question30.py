"""30. Write a Python program that will accept the base and height of a triangle 
and compute the area.

area of a triangle = 1/2 * b * h"""

b = int(input("Please Enter the base of the triangle: "))
h = int(input("Please Enter the height of the triangle: "))
area_of_a_triangle = b * h /2
print("area of a triangle:", area_of_a_triangle)